        <h1>Titulo</h1>

        <p>Un herbario es una colección de plantas preservadas. Los botánicos preservan las plantas que estudian para conservar su información genética, que creen les ayudará a comprender la diversidad de la vida en la tierra. Un herbario es un lugar para que los investigadores conserven y compartan especímenes. La recolección de plantas silvestres es una parte esencial de la botánica; sin ella, muchas plantas desaparecerían para siempre. Algunas personas, como los botánicos, recolectan especímenes de herbario para su conservación, identificación y estudio. Cualquiera puede convertirse en un maestro de su campo estudiando los especímenes en un herbario bien elaborado.</p>

        <p>Muchos jardines públicos, universidades y otras instituciones mantienen herbarios. Las plantas se recogen del medio natural y se conservan en alcohol o glicerina. Luego, estas plantas se etiquetan con información sobre la fuente, la preservación y la edad estimada del espécimen para que los científicos puedan identificarlo con precisión. Los herbarios a menudo se mantienen en bibliotecas o instalaciones exteriores lejos de condiciones ambientales dañinas. Las plantas en los herbarios se mantienen y almacenan cuidadosamente para evitar daños o descomposición.</p>

        <p>Los horticultores solían guardar los herbarios en gabinetes llamados cabinets des marques, que se usaban para conservar y etiquetar las hierbas. La palabra 'hierba' proviene de esta antigua palabra, francés para planta, herbarii eran botánicos que recolectaban plantas. La gente en el pasado hacía herbarios apilando sus hierbas en un gabinete para su conservación. Etiquetarían las plantas con información sobre dónde fueron recolectadas para que los investigadores pudieran encontrarlas nuevamente. Algunos herbarios estaban puramente ornamentados y llenos de hermosa cristalería, pero generalmente estaban reservados para patrones aristocráticos.</p>

        <h1>Subtitulo</h1>

        <p>La mayoría de los herbariens son médicos o científicos que viajan para recolectar especímenes para sus guías de campo y proyectos de investigación. Muchos también trabajan en instituciones públicas que albergan grandes herbarios, como universidades o jardines botánicos. El personal del herbario evalúa la calidad de una planta antes de enviarla a conservación para que sea tratada con cuidado. Cada espécimen tiene una etiqueta con información sobre dónde se encontró, cuándo se recolectó y cualquier dato adicional que el recolector quisiera documentar. Los científicos usan herbarios para encontrar nuevas especies y describirlas para que puedan ser estudiadas por otros científicos, publicadas y nombradas.</p>

        <p>La mayoría de los herbarianos están preocupados por preservar sus especímenes correctamente. Toman cursos especiales de capacitación sobre cómo preservar sus especímenes para que se vean perfectos cuando regresen al campo. Preservar las plantas es similar a la preservación de los seres humanos: debe preparar las muestras adecuadamente para evitar daños por condiciones de almacenamiento inadecuadas. También hay un nombre científico para cada condición de almacenamiento, de menor a mayor calidad, esto se llama congelación (-20 grados F), desecación (temperaturas bajo cero) y deshidratación (cero absoluto). La congelación (-20 grados F) es la mejor manera de preservar los especímenes de plantas, ya que conserva toda la integridad estructural de la planta y evita el daño causado por la retención de humedad. La desecación (-18 grados F) preserva la vida de la planta pero daña las partes de la planta más fácilmente que la congelación. La deshidratación (-17 grados F) preserva la vida de las plantas pero las daña más fácilmente que la congelación.</p>

